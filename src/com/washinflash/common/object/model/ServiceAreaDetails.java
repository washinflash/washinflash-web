package com.washinflash.common.object.model;

public class ServiceAreaDetails {

	private int serviceAreaId;
	private int serviceCityId;	
	private String serviceAreaCode;
	private String serviceAreaName;
	
	
	public int getServiceAreaId() {
		return serviceAreaId;
	}
	public void setServiceAreaId(int serviceAreaId) {
		this.serviceAreaId = serviceAreaId;
	}
	public int getServiceCityId() {
		return serviceCityId;
	}
	public void setServiceCityId(int serviceCityId) {
		this.serviceCityId = serviceCityId;
	}
	public String getServiceAreaCode() {
		return serviceAreaCode;
	}
	public void setServiceAreaCode(String serviceAreaCode) {
		this.serviceAreaCode = serviceAreaCode;
	}
	public String getServiceAreaName() {
		return serviceAreaName;
	}
	public void setServiceAreaName(String serviceAreaName) {
		this.serviceAreaName = serviceAreaName;
	}
	
}
