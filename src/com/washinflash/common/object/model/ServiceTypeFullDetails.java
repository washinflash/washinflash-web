package com.washinflash.common.object.model;

public class ServiceTypeFullDetails {

	private String serviceType;
	private String serviceTypeDesc;
	private String serviceStatus;
	
	private String vendorName;
	private String vendorMobileNo;
	private String vendorType;
	private String vendorEmail;
	private String vendorAddress;
	private String vendorPickupDate;
	
	
	public String getServiceType() {
		return serviceType;
	}
	public void setServiceType(String serviceType) {
		this.serviceType = serviceType;
	}
	public String getServiceTypeDesc() {
		return serviceTypeDesc;
	}
	public void setServiceTypeDesc(String serviceTypeDesc) {
		this.serviceTypeDesc = serviceTypeDesc;
	}
	public String getServiceStatus() {
		return serviceStatus;
	}
	public void setServiceStatus(String serviceStatus) {
		this.serviceStatus = serviceStatus;
	}
	public String getVendorName() {
		return vendorName;
	}
	public void setVendorName(String vendorName) {
		this.vendorName = vendorName;
	}
	public String getVendorMobileNo() {
		return vendorMobileNo;
	}
	public void setVendorMobileNo(String vendorMobileNo) {
		this.vendorMobileNo = vendorMobileNo;
	}
	public String getVendorType() {
		return vendorType;
	}
	public void setVendorType(String vendorType) {
		this.vendorType = vendorType;
	}
	public String getVendorEmail() {
		return vendorEmail;
	}
	public void setVendorEmail(String vendorEmail) {
		this.vendorEmail = vendorEmail;
	}
	public String getVendorAddress() {
		return vendorAddress;
	}
	public void setVendorAddress(String vendorAddress) {
		this.vendorAddress = vendorAddress;
	}
	public String getVendorPickupDate() {
		return vendorPickupDate;
	}
	public void setVendorPickupDate(String vendorPickupDate) {
		this.vendorPickupDate = vendorPickupDate;
	}
	
}
